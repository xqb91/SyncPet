    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if(txtRun.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Escriba el rut del propietario para buscar");
            txtRun.requestFocus();
        }else{
            if(cl.starlabs.herramientas.HerramientasRut.validar(txtRun.getText())) {
                //consultar por datos
                p = new PropietarioJpaController(emf).buscarPorRut(txtRun.getText());
                if(p == null) {
                    JOptionPane.showMessageDialog(null, "No hay propietarios registrados con el rut "+txtRun.getText());
                    txtRun.selectAll();
                    txtRun.requestFocus();
                    DefaultTableModel modelo = (DefaultTableModel) tablaResultados.getModel();
                    if(modelo.getRowCount() != 0)
                    {
                        for(int i = 0; i<modelo.getRowCount(); i++) {
                            modelo.removeRow(i);
                            p = null;
                        }
                    }
                    tablaResultados.setModel(modelo);
                }else{
                    //recibiendo valor de propietario
                    //Generando un modelo para a침adir los datos en una columna
                    DefaultTableModel modelo = (DefaultTableModel) tablaResultados.getModel();
                    //declarando arreglo para insertar los valores en la tabla
                    Object[] fila = new Object[5];
                    //rellenando los valores del arreglo con los valores que debe contener la tabla
                    fila[0] = cl.starlabs.herramientas.HerramientasRut.formatear(p.getRut()+""+p.getDv());
                    fila[1] = p.getApaterno()+" "+p.getAmaterno()+" "+p.getNombres();
                    //agregando la informaci칩n al modelo de la tabla
                    this.p = p;
                    modelo.addRow(fila);
                    //aplicando el modelo a la tabla actual
                    tablaResultados.setModel(modelo);
                }
            }else{
                txtRun.setForeground(Color.red);
                txtRun.selectAll();
                txtRun.requestFocus();
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void txtRunFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtRunFocusLost
        if(!txtRun.getText().isEmpty()) {
            txtRun.setText(cl.starlabs.herramientas.HerramientasRut.formatear(txtRun.getText()));
            if(cl.starlabs.herramientas.HerramientasRut.validar(txtRun.getText())) {
                btnBuscar.requestFocus();
                txtRun.setForeground(Color.black);
            }else{
                txtRun.setForeground(Color.red);
                txtRun.selectAll();
                txtRun.requestFocus();
            }
        }
    }//GEN-LAST:event_txtRunFocusLost

